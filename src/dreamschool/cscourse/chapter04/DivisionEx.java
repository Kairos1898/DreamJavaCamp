package dreamschool.cscourse.chapter04;

public class DivisionEx {

	public static void main(String[] args) {
		int number1 = 10;
		int number2 = 3;
		
		int 나누기결과 = number1 / number2;
		int 나머지 = number1 % number2;
		
		System.out.println(number1 +"/"+number2);
		System.out.println("나눗셈 결과 :" + 나누기결과 );
		System.out.println("나머지 :" + 나머지);

	}

}
